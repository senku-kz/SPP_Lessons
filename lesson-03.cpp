#include <stdio.h>

int main() {
    printf("Lesson 03 - Programs and Variables\n");
    // Programs and Variables

    // declaration
    int x, y;

    // declaration with initialization
    int z =  12;

    // assignments
    x = 23;
    y = x + 55;
    z = z - 100;


    // declaration
    int value1, value2, sum;

    // assignments
    value1 = 50;
    value2 = 25;
    sum = value1 + value2;

    printf("The sum of %i and %i is %i\n", value1, value2, sum);

    return 0;
}